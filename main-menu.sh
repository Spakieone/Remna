        echo -e "   ${GREEN}‚úÖ –î–æ—Å—Ç—É–ø–µ–Ω${NC}"
    else
        echo -e "   ${RED}‚ùå –ù–µ –Ω–∞–π–¥–µ–Ω${NC}"
