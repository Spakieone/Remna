        local docker_version=$(docker --version 2>/dev/null | cut -d' ' -f3 | cut -d',' -f1 || echo "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ")
        echo -e "   ${GRAY}üì¶ –í–µ—Ä—Å–∏—è: $docker_version${NC}"
